function menu(){
    if(nave.style.display == 'block'){
        nave.style.display = 'none'
    }
    else{
        nave.style.display = 'block'
    }
}

function tatela(){
    if(window.innerWidth >= 768){
        itens.style.display = 'block'
    }
    else{
        itens.style.display = 'none'
    }
}